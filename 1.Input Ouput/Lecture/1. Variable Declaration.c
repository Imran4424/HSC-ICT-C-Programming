#include <stdio.h>

int main()
{
    /// variable declaration
    int neha;
    /// value assign
    neha = 55;

    /// value print
    printf("Neha's value is - %d\n", neha);

    /// variable declaration
    float mark;
    /// value assign
    mark = 97.0;
    /// value print
    printf("Neha's mark is - %f\n", mark);

    /// variable declaration & value assign
    /// initialization
    double weight = 55;
    printf("Neha's weight is %lf\n", weight);

    /// variable declaration & value assign
    /// initialization
    char letter = 'N';
    printf("Neha's first letter is %c\n", letter);

    return 0;
}
/**
    Format specifier
    %d - int - Integer
    %f - float - Float
    %lf - double - Double
    %c - char - Character
*/







